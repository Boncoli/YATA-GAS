// tasks/do-daily-trend.js
/**
 * YATA カスタムタスク: 24時間限定デイリートレンド
 * 
 * 過去24時間の記事を対象に、履歴との比較を行わずに（Not History）
 * 本家の generateTrendReportHtml を使ってカード形式の深いレポートを生成・送信します。
 */

const path = require('path');
require('dotenv').config({ path: path.join(__dirname, '../.env'), override: true });

async function main() {
  console.log("--- YATA Daily Trend (24h Not History) Start ---");

  try {
    // 1. Bridge & YATA ロード
    require('../lib/gas-bridge.js');
    require('../lib/YATA.js');

    const keywords = (process.env.USER_KEYWORDS || "").split(',').map(k => k.trim());
    const mailTo = process.env.MAIL_TO;
    
    if (!mailTo || keywords.length === 0) {
      console.log("⚠️ MAIL_TO or USER_KEYWORDS is empty. Check .env.");
      return;
    }

    // 2. 過去24時間の関連記事を抽出 (DBを直接開く)
    const Database = require('better-sqlite3');
    const dbPath = process.env.DB_PATH || path.join(__dirname, '../yata.db');
    const db = new Database(dbPath);

    const now = new Date();
    const yesterday = new Date(now.getTime() - 24 * 60 * 60 * 1000);
    // YATAの collect テーブルは 'YYYY-MM-DD HH:MM:SS' 形式を期待
    const yesterdayStr = yesterday.toISOString().replace('T', ' ').substring(0, 19);

    let allTargetArticles = [];
    
    // 【最適化】全件投げず、キーワードのいずれかを含む記事のみをSQLで抽出する
    const keywordConditions = keywords.map(k => `title LIKE '%${k}%' OR summary LIKE '%${k}%' OR abstract LIKE '%${k}%'`).join(" OR ");
    
    const articles = db.prepare(`
      SELECT * FROM collect 
      WHERE date >= ? AND (${keywordConditions})
    `).all(yesterdayStr);
    
    // YATA.js が期待する Article オブジェクトの形式に整形
    articles.forEach(a => {
      allTargetArticles.push({
        id: a.id,
        date: new Date(a.date),
        title: a.title,
        url: a.url,
        abstract: a.abstract,
        summary: a.summary,
        source: a.source,
        vectorStr: a.vector
      });
    });

    if (allTargetArticles.length === 0) {
      console.log(`- No articles found in the last 24h for: ${keywords.join(", ")}`);
      return;
    }

    console.log(`- Found ${allTargetArticles.length} relevant articles. Generating Trend Report...`);

    // 3. ターゲットアイテムの作成
    const targetItems = keywords.map(kw => ({ query: kw, label: kw }));

    // 4. トレンドレポート生成 (聖域の関数を直接呼び出し)
    const reportHtml = generateTrendReportHtml(allTargetArticles, targetItems, yesterday, now, {
      useSemantic: (process.env.USE_SEMANTIC === "TRUE"),
      enableHistory: false, 
      saveHistory: false,
      dateRangeStr: `${fmtDate(yesterday)} 〜 ${fmtDate(now)}`,
      reasoning_effort: process.env.REASONING_MINI || "medium" // 🌟 思考レベルを medium に設定
    });

    // 5. メール送信
    if (reportHtml) {
      const subject = `【24h Daily Trend】 ${keywords.join(", ")} (${fmtDate(now)})`;
      console.log(`[Email] Sending trend report to ${mailTo}...`);
      
      // GmailApp (gas-bridge.js が提供) で送信
      GmailApp.sendEmail(mailTo, subject, "", {
        htmlBody: reportHtml
      });
      console.log("✅ 24h Daily Trend Report sent successfully.");
    } else {
      console.log("ℹ️ No significant trends generated by LLM.");
    }

  } catch (e) {
    console.error("\n❌ Error during daily trend generation:", e);
  }

  console.log("--- YATA Daily Trend Task Finished ---");
}

main().catch(console.error);
